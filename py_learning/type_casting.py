# Type Casting
# int() - Constructs an Int Num from an integer literal, a float literal (removing decimals), or a string literal (provinding the string represented a whole num)
# float() - Constructs a float num from an int literal, a float literal or a string literal (providing the string represents a float or an integer)
# str() - Constructs a string from a wide vcariety of data types, including strings, int literals and float literals.

x = int(1) # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3

x = float(1) # x will be 1.0
y = float(2.8) # y will be 2.8
z = float("3") # z will be 3.0
w = float("4.2") # w will be 4.2

x = str("s1") # x will be 's1'
y = str(2) # y will be '2'
z = str(3.0) # z will be '3.0'

